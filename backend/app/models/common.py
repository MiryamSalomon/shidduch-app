"""
Common Model Utilities
=======================
Shared types, enums, and base classes used across all domain models.

This module solves three cross-cutting problems:

1. **ObjectId handling** â€” MongoDB uses ``bson.ObjectId`` as its primary key
   type, but Pydantic v2 and JSON don't natively support it. We define a
   custom ``PyObjectId`` annotation that validates ObjectId strings on input,
   serialises them as strings in JSON responses, and passes through real
   ObjectIds when talking to MongoDB.

2. **Enums** â€” Centralised string enums for fields like ``gender``, ``status``,
   ``community``, ``role``, and ``suggestion_status``. Keeping them here avoids
   circular imports and gives the frontend a single reference for dropdown values.

3. **Base models** â€” ``MongoBaseModel`` adds ``id``, ``created_at``, and
   ``updated_at`` fields that every MongoDB document shares, plus JSON
   serialisation config for ObjectIds.

Usage::

    from app.models.common import PyObjectId, Gender, MongoBaseModel

    class CandidateInDB(MongoBaseModel):
        first_name: str
        gender: Gender
"""

from datetime import datetime
from enum import Enum
from typing import Annotated, Any

from bson import ObjectId
from pydantic import BaseModel, BeforeValidator, ConfigDict, Field


# =============================================================================
# ObjectId Handling
# =============================================================================

def _validate_object_id(value: Any) -> ObjectId:
    """
    Validate that a value is a valid MongoDB ObjectId.

    Accepts:
        - A ``bson.ObjectId`` instance (passthrough).
        - A 24-character hex string (converted to ObjectId).

    Args:
        value: The raw value to validate.

    Returns:
        A ``bson.ObjectId`` instance.

    Raises:
        ValueError: If the value is not a valid ObjectId or hex string.
    """
    if isinstance(value, ObjectId):
        return value
    if isinstance(value, str) and ObjectId.is_valid(value):
        return ObjectId(value)
    raise ValueError(f"Invalid ObjectId: {value}")


# Annotated type that Pydantic uses for validation and serialisation.
# - BeforeValidator runs _validate_object_id before Pydantic's own checks.
# - In JSON output, ObjectIds are serialised as plain strings (24-char hex).
PyObjectId = Annotated[ObjectId, BeforeValidator(_validate_object_id)]


