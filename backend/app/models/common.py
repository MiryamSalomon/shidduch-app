class MongoBaseModel(BaseModel):
    """
    Base model for all documents stored in MongoDB.

    Provides three universal fields:
        - ``id``: maps to MongoDB's ``_id`` field (aliased).
        - ``created_at``: timestamp when the document was first inserted.
        - ``updated_at``: timestamp of the last modification.

    Configuration:
        - ``populate_by_name=True``: allows using either ``id`` or ``_id``
          when constructing from a Mongo document.
        - ``arbitrary_types_allowed=True``: permits ``bson.ObjectId`` in fields.
        - ``json_encoders``: serialises ObjectId as string in JSON responses.
    """

    model_config = ConfigDict(
        populate_by_name=True,
        arbitrary_types_allowed=True,
        json_encoders={ObjectId: str},
    )

    id: PyObjectId = Field(default_factory=ObjectId, alias="_id")
    created_at: datetime = Field(default_factory=datetime.utcnow)
    updated_at: datetime = Field(default_factory=datetime.utcnow)


class PaginatedResponse(BaseModel):
    """
    Generic wrapper for paginated list responses.

    Used by all list endpoints (GET /candidates, GET /suggestions, etc.)
    to return items alongside pagination metadata.

    Attributes:
        items: The list of results for the current page.
        total: Total number of matching documents across all pages.
        page: Current page number (1-based).
        page_size: Number of items per page.
        total_pages: Total number of pages (computed from total / page_size).
    """

    items: list[Any]
    total: int
    page: int
    page_size: int
    total_pages: int
